package com.xworkz.apti;

public class ReverseString {
	public static void main(String[] args) {

		// Reverse a string
		String[] a = { "getting ", "good ", "at ", "coding ", "needs ", "a ", "lot ", "of ", "practice " };
		

		for (int i = a.length - 1; i >= 0; i--) {
			System.out.print(a[i]);
		}
		
		
		
	}

}
